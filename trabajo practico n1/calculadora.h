/** \brief la funcion pide numeros
 *
 * \param num float ingresa operandos.
 * \return float retorna el operando ingresado.
 *
 */
float obtenerNumeros(float num);

/** \brief esta funcion lo que hace es sumar los diferentes operandos.
 *
 * \param a float primer operando.
 * \param b float segundo operando.
 * \return float retorna el resultado de la suma.
 *
 */
float suma(float a, float b);

/** \brief esta funcion resta los diferentes operandos.
 *
 * \param a float primer operando.
 * \param b float segundo operando.
 * \return float retorna el resultado de la resta
 *
 */
float resta(float a, float b);

/** \brief esta funcion lo que hace es dividir los operandos.
 *
 * \param a float primer operando ingresado.
 * \param b float segundo operando ingresado.
 * \return float retorna el resultado de la division
 *
 */
float division(float a, float b);

/** \brief lo que hace esta funcion es multiplicar los operandos
 *
 * \param a float primer operando.
 * \param b float segundo operando.
 * \return float retorna la el resultado de la multiplicacion.
 *
 */
float multiplicacion(float a, float b);

/** \brief lo que hace esta funcion es mostrar el factorial de cada operando
 *
 * \param num float operando ingresado.
 * \return long long int retorna el resultado del factorial.
 *
 */
long long int factorial(float num);
